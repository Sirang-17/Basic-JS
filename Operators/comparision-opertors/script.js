// Comparison Operators
let m = 7;
let n = 10;

console.log("==", m == n);
console.log("!=", m != n);
console.log("===", m === n);  //check value + datatype
console.log("!==", m !== n);
console.log(">", m > n);
console.log("<", m < n);
console.log(">=", m >= n);
console.log("<=", m <= n);

